void EXTI0_IRQHandler(void)
{
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_0);
}
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) 
{
  if		 (GPIO_Pin == GPIO_PIN_2){KeyState = PAUSE;}	  /*key S5*/
	else if(GPIO_Pin == GPIO_PIN_0){KeyState = PREVIOUS;} /*key S1*/						
	else if(GPIO_Pin == GPIO_PIN_1){KeyState = DECREMENT;}/*encoder 1*/
	else if(GPIO_Pin == GPIO_PIN_3){KeyState = STOP;}		  /*key S2*/
	else if(GPIO_Pin == GPIO_PIN_4){KeyState = NEXT;}			/*key S4*/
	else if(GPIO_Pin == GPIO_PIN_5){KeyState = PLAY;}			/*key S6*/
	else if(GPIO_Pin == GPIO_PIN_8){KeyState = INCREMENT;}/*encoder 2*/
	else if(GPIO_Pin == GPIO_PIN_9){KeyState = MUTE;}		  /*key S3*/
}

